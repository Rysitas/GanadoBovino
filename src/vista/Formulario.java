    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        Icon icono3 = new ImageIcon(getClass().getResource("/Imagenes/CierreSesion.png"));

        if (this.seleccion.getSelectedIndex() == 0 || this.txtCode.getText() == "" || this.txtPeso.getText() == "" || this.txtEdad.getText() == "") {
            JOptionPane.showMessageDialog(null, "Llene los datos adecuadamente.", "Adventencia.", JOptionPane.INFORMATION_MESSAGE, icono3);
        } else {
            int modif = Integer.parseInt(JOptionPane.showInputDialog(null, "Digite el Código del Bovino al cual desea Modificar: "));

            int code = Integer.parseInt(this.txtCode.getText());
            String tipo = this.seleccion.getSelectedItem().toString();
            float peso = Float.parseFloat(this.txtPeso.getText());
            int edad = Integer.parseInt(this.txtEdad.getText());

            try {

                PreparedStatement pps = cn.prepareStatement("UPDATE GANADO SET CODIGO='" + code + "',TIPO='" + tipo + "',PESO='" + peso + "', EDAD='" + edad + "' WHERE CODIGO='" + modif + "';");
                pps.executeUpdate();
                JOptionPane.showMessageDialog(null, "Datos Actualizados Exitosamente");
                this.limpiar();
                this.LlenarTabla(jTable2);
            } catch (SQLException ex) {
                Logger.getLogger(Formulario.class.getName()).log(Level.SEVERE, null, ex);
            }

            txtCode.setText("");
            txtEdad.setText("");
            txtPeso.setText("");
        }

    }//GEN-LAST:event_jButton5ActionPerformed

