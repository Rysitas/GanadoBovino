    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (seleccion.getSelectedIndex() != 0 && txtCode.getText() != "" && txtEdad.getText() != "" && txtPeso.getText() != "") {
            Ganado gan = new Ganado();
            GanadoCrud ganC = new GanadoCrud();
            gan.setCcodigo(Integer.parseInt(txtCode.getText()));
            gan.setTipo(seleccion.getSelectedItem().toString());
            gan.setPeso(Float.parseFloat(txtPeso.getText()));
            gan.setEdad(Integer.parseInt(txtEdad.getText()));
            if (ganC.create(gan)) {
                JOptionPane.showMessageDialog(null, "Registro Guardado");
                LlenarTabla(jTable2);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Rellene los campos.");
        }
        txtCode.setText("");
        txtEdad.setText("");
        txtPeso.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed

